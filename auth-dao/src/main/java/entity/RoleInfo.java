public class RoleInfo {
    private int rid;
    private String rname;
    @Override
    public String toString() {
        return "RoleInfo{" +
                "rid=" + rid +
                ", rname='" + rname + '\'' +
                '}';
    }
    public int getRid() {
        return rid;
    }

    public void setRid(int rid) {
        this.rid = rid;
    }

    public String getRname() {
        return rname;
    }

    public void setRname(String rname) {
        this.rname = rname;
    }
}
